class Solution:
    def isValidSudoku(self, board) -> bool:
        return (self._is_row_valid(board) and self._is_col_valid(board) and self._is_square_valid(board))

    def _is_row_valid(self, board):
        for row in board:
            if not self._is_valid_line(row):
                return False
        return True

    def _is_col_valid(self, board):
        for col in zip(*board):
            print(col)
            if not self._is_valid_line(col):
                return False
        return True

    def _is_square_valid(self, board):
        for i in (0, 3, 6):
            for j in (0, 3, 6):
                square = [board[x][y] for x in range(i, i + 3) for y in range(j, j + 3)]
                if not self._is_valid_line(square):
                    return False
        return True


    def _is_valid_line(self, line):
        elements = [ele for ele in line if ele != '.']
        return len(set(elements)) == len(elements)


        
